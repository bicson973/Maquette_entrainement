// EXEMPLE DE JAVASCRIPT ANCIEN :

var unTexte = "Voici un texte";
// J'initialise ma variable : 
// je crée ma boite avec -> var / Nom de ma boite -> unTexte / qui contient -> = / Valeur contenue dans ma boite -> "Voici un texte".
console.log(unTexte);

unTexte = "Nouveau texte"; 
// J'affecte une nouvelle valeur à ma variable. 
console.log(unTexte);

// EXEMPLE DE JAVASCRIPT MODERNE :

const prenom = "Séma";
// Une constante ne bouge jamais, on ne peut pas lui affecter une nouvelle valeur.
console.log(prenom);

let unChiffre = 3; 
// J'initialise ma variable let : celle-ci contrairement à "const" on peut lui affacter une autre valeur. 
console.log(unChiffre);

unChiffre = 9; 
// J'affecte une nouvelle valeur à ma variable let. 
console.log(unChiffre);


